SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS C
JOIN TB_CLASS_PROFESSOR CP USING( CLASS_NO )
JOIN TB_PROFESSOR USING( PROFESSOR_NO )
JOIN TB_DEPARTMENT D ON( D.DEPARTMENT_NO = C.DEPARTMENT_NO )
WHERE CATEGORY = '인문사회';

