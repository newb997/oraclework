SELECT CLASS_NAME, DEPARTMENT_NAME
FROM TB_CLASS
LEFT JOIN TB_CLASS_PROFESSOR USING( CLASS_NO )
LEFT JOIN TB_DEPARTMENT USING( DEPARTMENT_NO )  --NULL값도 나와야되면 LEFT JOIN이나 RIGHT JOIN을 써야함
WHERE CATEGORY = '예체능'
AND PROFESSOR_NO IS NULL;

